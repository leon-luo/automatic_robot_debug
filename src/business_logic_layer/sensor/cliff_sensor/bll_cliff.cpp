/*****************************************************************************
 函 数 名: bll_cliff.get_instance
 功能描述  : 获取实例
 输入参数: void  
 输出参数: 无
 返 回 值: bll_cliff*
 
 修改历史:
  1.日     期: 2017年12月21日
    作     者: Leon
    修改内容: 新生成函数
*****************************************************************************/
bll_cliff* bll_cliff::get_instance(void)
{
	if (nullptr == p_instance_)
	{
		pthread_mutex_lock(&mutex_);
		if (nullptr == p_instance_)
		{
			p_instance_ = new bll_cliff();
		}
		pthread_mutex_unlock(&mutex_);
	}
	return p_instance_;
}

/*****************************************************************************
 函 数 名: bll_cliff.release_instance
 功能描述  : 释放实例
 输入参数: void  
 输出参数: 无
 返 回 值: void
 
 修改历史:
  1.日     期: 2017年12月21日
    作     者: Leon
    修改内容: 新生成函数
*****************************************************************************/
void bll_cliff::release_instance(void)
{
	if (nullptr != p_instance_)
	{
		pthread_mutex_lock(&mutex_);
		if (nullptr != p_instance_)
		{
			delete p_instance_;
			p_instance_ = nullptr;
		}
		pthread_mutex_unlock(&mutex_);
	}
}

/*****************************************************************************
 函 数 名: bll_cliff.upate_cliff_state
 功能描述  : 更新悬崖传感器状态
 输入参数: uint8_t id     
           uint8_t value  
 输出参数: 无
 返 回 值: void
 
 修改历史:
  1.日     期: 2017年12月14日
    作     者: Leon
    修改内容: 新生成函数
*****************************************************************************/
void bll_cliff::upate_cliff_state(uint8_t id, uint8_t value)
{
	bool flag = false;
	bool cliff_state = false;
	CLIFF_ID_ENUM cliff_id;

	flag = cfg_if_convert_cliff_id(id, cliff_id);
	if (false == flag)
	{
		debug_print_warnning("id = %d", id);
		return;
	}

	flag = cfg_if_convert_cliff_state(value, cliff_state);
	if (false == flag)
	{
		debug_print_warnning("value = %d", value);
		return;
	}

	cfg_if_set_cliff_state(cliff_id, cliff_state);
	
	debug_print_info("cliff_id=%d, cliff_state=%d", cliff_id, cliff_state);
}

