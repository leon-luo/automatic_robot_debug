	void straight_moving(double line_v);
	void rotate_moving(double angular_v);
	
	void stop(void);
	void go_forward(void);
	void go_back(void);
	void turn_left(void);
	void turn_right(void);
	void pivot(void);

	void turn_back_clockwise(void);
